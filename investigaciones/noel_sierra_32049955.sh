echo "    en comparación con los sistemas de almacenamiento."
echo ""
echo "* Ventajas:"
echo "  - Permite un acceso inmediato y aleatorio a cualquier celda de memoria"
echo "    sin penalización de tiempo."
echo "  - Crucial para la fluidez del sistema multitarea."
echo ""
echo "* Desventajas:"
echo "  - Capacidad limitada y costo por gigabyte significativamente más alto"
echo "    que el almacenamiento secundario."
echo "  - No sirve para guardar archivos de forma permanente debido a su volatilidad."
echo ""

echo "========================================================================="
echo "4. DISCO DE ALMACENAMIENTO (HDD / SSD)"
echo "========================================================================="
echo "Son los encargados del almacenamiento secundario no volátil, manteniendo"
echo "resguardados los datos, el sistema operativo y las aplicaciones de forma"
echo "permanente, incluso sin energía eléctrica."
echo ""
echo "* Características:"
echo "  - Mecánicos (HDD): Utilizan platos magnéticos giratorios y cabezales lectores."
