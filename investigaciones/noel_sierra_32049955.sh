echo "    partes móviles y multiplicando la velocidad."
echo ""
echo "* Ventajas:"
echo "  - Altas capacidades de almacenamiento (Terabytes) a costos accesibles."
echo "  - Retención permanente y segura de los datos del usuario."
echo ""
