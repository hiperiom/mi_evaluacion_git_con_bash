echo "========================================================================="
echo "2. LA UNIDAD CENTRAL DE PROCESAMIENTO (CPU)"
echo "========================================================================="
echo "La CPU es el cerebro del computador, encargada de interpretar y ejecutar"
echo "las instrucciones de los programas mediante ciclos de lectura, decodificación"
echo "y ejecución de datos."
echo ""
echo "* Características:"
echo "  - Velocidad de Relioj: Medida en Gigahertz (GHz), indica cuántos ciclos"
