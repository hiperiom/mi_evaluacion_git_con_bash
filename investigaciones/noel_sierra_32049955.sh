echo ""
echo "* Desventajas:"
echo "  - Generación de calor elevado que requiere sistemas de enfriamiento."
echo "  - Su velocidad se ve limitada si los demás componentes (RAM/Disco)"
echo "    provocan un cuello de botella."
echo ""

echo "========================================================================="
echo "3. MEMORIA DE ACCESO ALEATORIO (RAM)"
echo "========================================================================="
echo "La RAM es la memoria de trabajo volátil del sistema. Almacena de forma"
echo "temporal los datos y programas que la CPU está procesando activamente en"
echo "el momento."
echo ""
echo "* Características:"
echo "  - Volatilidad: Pierde absolutamente toda la información guardada"
echo "    cuando el dispositivo se apaga o se reinicia."
